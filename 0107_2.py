import re

# 標準入力で "255.255.255.255"
s = input()
# {}で繰り返し処理を行う事でコードを短くする
x = re.fullmatch(r"([2][0-5]{,2}|[1][\d]{,2}|[\d]{,2})"
                 r"\.([2][0-5]{,2}|[1][\d]{,2}|[\d]{,2})"
                 r"\.([2][0-5]{,2}|[1][\d]{,2}|[\d]{,2})"
                 r"\.([2][0-5]{,2}|[1][\d]{,2}|[\d]{,2})",
                 s)
# 0.0.0.0 ～ 255.255.255.255 の範囲内であれば True
# 範囲外であれば　Falseを返す
if x is None:
    print(False)
else:
    print(True)