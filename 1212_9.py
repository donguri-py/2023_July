import re

#標準入力で "255.255.255.255"
s = input()
x = re.fullmatch(r"([1|2][1-5][1-5]|[\d][\d]|[\d])"
                     r"\.([1|2][1-5][1-5]|[\d][\d]|[\d])"
                     r"\.([1|2][1-5][1-5]|[\d][\d]|[\d])"
                     r"\.([1|2][1-5][1-5]|[\d][\d]|[\d])",
                     s)
#0.0.0.0 ～ 255.255.255.255 の範囲内であれば True 範囲外であれば　Falseを返す
if x is None:
    print(False)
else:
    print(True)